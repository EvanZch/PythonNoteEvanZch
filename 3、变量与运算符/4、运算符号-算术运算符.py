###### 运算符号


#### 算术运算符
###  +
print("hello"+"world")   # helloworld

### *
print("hello"*4)  # hellohellohellohello
### -
print(3-1)  #2

### 除 / 和 整除 //
print(5/2)  # 2.5
print(5//2)  # 2
### 余数  %
print(5 %2)  # 1

### 立方，平方
## 2的平方
print(2**2)  # 4
## 2的立方
print(2**3)  # 8
## 2的100次方
print(2**100)  # 1267650600228229401496703205376


