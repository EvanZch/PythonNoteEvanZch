########## 位运算符

####### 把数值当成二进制数进行运算

##### & 按位与
a=2
b=3
## 需要分别把数值转成2进制进行计算 a = 0b10  b = 0b11
## 这里展示计算结果，同时为1才为1，其他为零，得出结果为ob10
## a 1 0
## b 1 1
## = 1 0
print( a & b)  #2

##### | 按位或

a = 2
b = 3
## a 1 0  ---> a 转为2进制
## b 1 1  ---> b 转为2进制
## = 1 1  ---> 十进制3
print(a | b)  # 3
