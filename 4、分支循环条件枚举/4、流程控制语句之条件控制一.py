# mood 心情
mood = True
# 注意这里的空格，python是使用缩进来判断语句代码块
if mood:
    print("go to left")
    print("play")
else:
    print("go to right")

# 如果mood为True，输出：
# go to left
# play
#
# 如果mood为False ,输出：
# go to right

